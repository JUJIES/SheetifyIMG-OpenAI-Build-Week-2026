  let activeLocale = DEFAULT_LOCALE;

  function supported(value) {
    const candidate = String(value || "")
      .trim()
      .toLowerCase()
      .replace(/_/g, "-")
      .split("-", 1)[0];
    return SUPPORTED_LOCALES.includes(candidate) ? candidate : null;
  }

  function normalize(value, fallback = DEFAULT_LOCALE) {
    return supported(value) || supported(fallback) || DEFAULT_LOCALE;
  }

  function queryLocale(query) {
    if (query && typeof query.get === "function") {
      return query.get("lang");
    }
    const value = String(query || "").trim();
    if (!value.includes("=") && !value.startsWith("?")) {
      return value;
    }
    return new URLSearchParams(value.replace(/^\?/, "")).get("lang");
  }

  function browserLocales(value) {
    return Array.isArray(value) ? value : [value];
  }

  function resolve(input = {}) {
    const candidates = [
      queryLocale(input.query),
      input.stored,
      input.session,
      input.invitation,
      ...browserLocales(input.browser)
    ];
    return candidates.map(supported).find(Boolean) || DEFAULT_LOCALE;
  }

  function storage() {
    try {
      return root?.localStorage || null;
    } catch {
      return null;
    }
  }

  function current() {
    return activeLocale;
  }

  function set(value, options = {}) {
    activeLocale = normalize(value);
    if (options.persistLocal !== false) {
      storage()?.setItem(STORAGE_KEY, activeLocale);
    }
    if (root?.document?.documentElement) {
      root.document.documentElement.lang = activeLocale;
    }
    return activeLocale;
  }

  function interpolate(value, variables = {}) {
    return String(value).replace(/\{\{([A-Za-z0-9_]+)\}\}/g, (_match, name) => (
      Object.prototype.hasOwnProperty.call(variables, name) ? String(variables[name]) : ""
    ));
  }

  function t(key, variables = {}, locale = activeLocale) {
    const normalized = normalize(locale);
    const value = CATALOGUES[normalized]?.[key] ?? CATALOGUES[DEFAULT_LOCALE]?.[key] ?? key;
    return interpolate(value, variables);
  }

  function apply(rootElement = root?.document) {
    if (!rootElement?.querySelectorAll) return;
    rootElement.querySelectorAll("[data-i18n]").forEach((element) => {
      element.textContent = t(element.dataset.i18n);
    });
    for (const [attribute, dataAttribute] of [
      ["placeholder", "i18nPlaceholder"],
      ["title", "i18nTitle"],
      ["aria-label", "i18nAriaLabel"]
    ]) {
      rootElement.querySelectorAll(`[data-${dataAttribute.replace(/[A-Z]/g, (letter) => `-${letter.toLowerCase()}`)}]`)
        .forEach((element) => element.setAttribute(attribute, t(element.dataset[dataAttribute])));
    }
  }

  return Object.freeze({
    CATALOGUES,
    DEFAULT_LOCALE,
    STORAGE_KEY,
    SUPPORTED_LOCALES,
    apply,
    current,
    normalize,
    resolve,
    set,
    t
  });
